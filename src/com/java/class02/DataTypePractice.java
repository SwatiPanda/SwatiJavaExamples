package com.java.class02;
public class DataTypePractice {
    public static void main(String[] args) {
            byte num1 = 127;
            short num2 = 32767;
            int num3 = 889653583;
            long num4 = 675657879;
            double num5 = 89.78757556654788996;
            float num6 = 56.8967645645646456464576f;
            System.out.println("byte is "+num1);
            System.out.println("short is "+num2);
            System.out.println("int is "+num3);
            System.out.println("long is "+num4);
            System.out.println("double is "+num5);
            System.out.println("float is "+num6);
        }
    }


